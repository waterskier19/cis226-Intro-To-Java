public class PredefinedMethods {
    public static void main(String[] args) {
        double u, v;
        System.out.println("line 1:  2 to the power of 6 = " + Math.pow(2, 6));

        u = 12.5;
        v = 3.0;
        System.out.println("Line 4: " + u + " to the power of " + v + " = " + Math.pow(u, v));

        System.out.println("Line 5: square root of 42.25 = " + Math.sqrt(42.25));

        u = Math.pow(8.5, 2.0);
        System.out.println("Line 7: u = " + u);

    } // end main
}